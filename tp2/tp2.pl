	sem_operadoras(Nodo, Dst, L, Caminho).
	
sem_operadoras(Dst, Dst, B, [Dst|Caminho]) :- findall((O),paragem(Dst,_,_,_,_,_,O,_,_,_,_),L),
										 nao(conf_list(B,L)).
													
