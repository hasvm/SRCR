	adjacente(Nodo, ProxNodo,_),
	findall((O),paragem(Nodo,_,_,_,_,_,O,_,_,_,_),L),
	nao(conf_list(B,L)),
	sem_operadoras(ProxNodo, Dst, B, Caminho).
	
	
